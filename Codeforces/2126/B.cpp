// disjoint set union by  & path compression
struct dsur_t {

    std::vector<int64_t> parent, rank;

    dsur_t(int64_t n) {
        // O(n)
        parent.resize(n + 1);
        // O(n)
        rank.resize(n + 1, 0);

        for (int64_t i = 1; i <= n; ++i) {
            parent[i] = i;
        }
    }
    // O(1)
    int64_t find_parent(int64_t x) {
        if (parent[x] != x)
            parent[x] = find_parent(parent[x]);
        return parent[x];
    }

    void unite(int64_t u, int64_t v) {
        u = find_parent(u);
        v = find_parent(v);

        if (v == u)
            return;

        if (rank[u] < rank[v])
            std::swap(u, v);

        parent[v] = u;
        if (rank[u] == rank[v])
            ++rank[u];
    }
};

void solve() {
    int n, k;
    std::cin >> n >> k;
    std::vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        std::cin >> a[i];
    }

    std::vector<int> prefix_ones(n + 1, 0);
    for (int i = 0; i < n; ++i) {
        prefix_ones[i + 1] = prefix_ones[i] + a[i];
    }

    std::vector<int> dp(n + 3, 0);

    for (int i = n; i >= 1; --i) {
        dp[i] = dp[i + 1];

        if (i + k - 1 <= n) {
            int ones_in_window = prefix_ones[i + k - 1] - prefix_ones[i - 1];
            if (ones_in_window == 0) {
                dp[i] = std::max(dp[i], 1 + dp[i + k + 1]);
            }
        }
    }

    std::cout << dp[1] << "\n";
}

