// disjoint set union by  & path compression
struct dsur_t {

    std::vector<int64_t> parent, rank;

    dsur_t(int64_t n) {
        // O(n)
        parent.resize(n + 1);
        // O(n)
        rank.resize(n + 1, 0);

        for (int64_t i = 1; i <= n; ++i) {
            parent[i] = i;
        }
    }
    // O(1)
    int64_t find_parent(int64_t x) {
        if (parent[x] != x)
            parent[x] = find_parent(parent[x]);
        return parent[x];
    }

    void unite(int64_t u, int64_t v) {
        u = find_parent(u);
        v = find_parent(v);

        if (v == u)
            return;

        if (rank[u] < rank[v])
            std::swap(u, v);

        parent[v] = u;
        if (rank[u] == rank[v])
            ++rank[u];
    }
};

/*
3 5 15 -> 1 move
3 5 2 
    3 - 2, 5 - 2 -> 1, 3 +1 move
    1 - 1, 3 - 2 -> 0, 1 +1 move
    0 - 0, 1 - 1 -> 0, 0 +1 move
    5 is the max, floor(5/2) which is 2, + 1 is the answer

9 7 5
    max = 9, floor(9/5) = 1, answer = 2
*/
int64_t gcd(int64_t a, int64_t b) {
    while (b != 0) {
        int64_t temp = b;
        b = a % b;
        a = temp;
    }
    return a;
}

void solve() {
    int64_t a, b, k;
    std::cin >> a >> b >> k;

    int64_t g = gcd(a, b);

    if (std::max(a / g, b / g) > k) {
        std::cout << 2 << '\n';
    } else {
        std::cout << 1 << '\n';
    }
}

